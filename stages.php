	<h4> Le sujet :</h4>
	<pre>

	Nous recherchons pour un client grand compte un développeur JAVA pour travailler en collaboration 
	avec une équipe de développeurs  (environ 8 personnes) sur des projets de plusieurs types.
			

	Vous travaillerez sur le développement de nouvelles fonctionnalités et vous contribuerez 
	à être force de proposition sur des choix technologiques.
	</pre>
	<h4> Mission :</h4>
	<pre>
	
	En tant que MOE junior, vous serez amené à développer en Java des modules d’un moteur de règles 
	liés aux flux de messages SWIFT, composante d’un progiciel Groupaxis de gestion d’un système 
	de routage SWIFT 
	
	Une fois la logique de fonctionnement du système appréhendée, le poste sera amené à évoluer 
	vers un poste de Chef de Projet Junior MOE qui consistera à prendre en charge l’évolution
	et la maintenance du Progiciel.
	</pre>
	<h4> Tâches :</h4>
	<pre>

	-Développement sur les technologies Java, ElasticSearch, Services REST, AKKA, Scala 
	-Bases de données Postgress SQL 
	-Développement XML 
	-Acquisition des normes ISO 15022 et ISO 20022 
	-Exécution des tests 
	-Rédaction des documentations techniques et Clients 
	-Coordination avec tous les acteurs impliqués (Experts internes, MOA et Clients) 
	-Assistance Client et suivi de production
	</pre> 
